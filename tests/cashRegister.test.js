test('-------- Test 1', (assert) => {
    const expected = {status: "OPEN", change: [["QUARTER", 0.5]]};
    const message  = `After checkCashRegister(19.5, 20, [["PENNY", 1.01], ["NICKEL", 2.05], ["DIME", 3.1], ["QUARTER", 4.25], ["ONE", 90], ["FIVE", 55], ["TEN", 20], ["TWENTY", 60], ["ONE HUNDRED", 100]]) should return ${JSON.stringify(expected)}.`;

    const actual = checkCashRegister(19.5, 20, [["PENNY", 1.01], ["NICKEL", 2.05], ["DIME", 3.1], ["QUARTER", 4.25], ["ONE", 90], ["FIVE", 55], ["TEN", 20], ["TWENTY", 60], ["ONE HUNDRED", 100]]);

    assert.deepEquals(actual, expected, message);
    assert.end();
});

